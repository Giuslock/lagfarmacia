package org.univaq.oop.business;

public class NonCiSonoAbbastanzaFarmaciException extends BusinessException {

    public NonCiSonoAbbastanzaFarmaciException() {
        super();
    }

    public NonCiSonoAbbastanzaFarmaciException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
        super(message, cause, enableSuppression, writableStackTrace);
    }

    public NonCiSonoAbbastanzaFarmaciException(String message, Throwable cause) {
        super(message, cause);
    }

    public NonCiSonoAbbastanzaFarmaciException(String message) {
        super(message);
    }

    public NonCiSonoAbbastanzaFarmaciException(Throwable cause) {
        super(cause);
    }
}
